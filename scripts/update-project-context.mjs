import fs from "node:fs/promises";
import path from "node:path";

const ROOT = process.cwd();
const OUTPUT_FILE = path.join(ROOT, "PROJECT_CONTEXT.md");
const TREE_MAX_DEPTH = 4;

const IGNORE_DIRS = new Set([
  ".git",
  "node_modules",
  "dist",
  ".vscode",
]);

const IMPORTANT_PATHS = [
  "README.md",
  "GUIA_INSTALACION.md",
  "INICIO_RAPIDO.md",
  "docs/especificacion-proyecto.md",
  "docs/base-datos-supabase.md",
  "supabase/schema.sql",
  "src/main.jsx",
  "src/App.jsx",
  "src/pages/ValesPage.jsx",
  "src/pages/BancoPage.jsx",
  "src/pages/ConfiguracionPage.jsx",
  "src/context/ClientsContext.jsx",
  "src/domain/personal/paymentDates.js",
  "package.json",
  "vite.config.js",
  "tailwind.config.js",
];

const MANUAL_START = "<!-- MANUAL_NOTES_START -->";
const MANUAL_END = "<!-- MANUAL_NOTES_END -->";

async function pathExists(targetPath) {
  try {
    await fs.access(targetPath);
    return true;
  } catch {
    return false;
  }
}

async function readJson(filePath) {
  try {
    const text = await fs.readFile(filePath, "utf8");
    return JSON.parse(text);
  } catch {
    return null;
  }
}

function formatList(items) {
  if (!items || items.length === 0) {
    return "- (none)";
  }
  return items.map((item) => `- ${item}`).join("\n");
}

async function readDirectoryTree(dirPath, depth = 0) {
  if (depth > TREE_MAX_DEPTH) {
    return [];
  }

  const entries = await fs.readdir(dirPath, { withFileTypes: true });
  const filtered = entries
    .filter((entry) => !IGNORE_DIRS.has(entry.name))
    .sort((a, b) => {
      if (a.isDirectory() && !b.isDirectory()) return -1;
      if (!a.isDirectory() && b.isDirectory()) return 1;
      return a.name.localeCompare(b.name);
    });

  const lines = [];

  for (const entry of filtered) {
    const prefix = "  ".repeat(depth);
    const entryPath = path.join(dirPath, entry.name);

    if (entry.isDirectory()) {
      lines.push(`${prefix}- ${entry.name}/`);
      if (depth < TREE_MAX_DEPTH) {
        const childLines = await readDirectoryTree(entryPath, depth + 1);
        lines.push(...childLines);
      }
    } else {
      lines.push(`${prefix}- ${entry.name}`);
    }
  }

  return lines;
}

async function collectImportantFileStatus() {
  const lines = [];

  for (const relativePath of IMPORTANT_PATHS) {
    const absolutePath = path.join(ROOT, relativePath);
    const exists = await pathExists(absolutePath);
    lines.push(`- ${relativePath}: ${exists ? "present" : "missing"}`);
  }

  return lines.join("\n");
}

function extractManualNotes(existingContent) {
  if (!existingContent) {
    return "- Add project-specific notes here.\n- This section is preserved across regenerations.";
  }

  const start = existingContent.indexOf(MANUAL_START);
  const end = existingContent.indexOf(MANUAL_END);

  if (start === -1 || end === -1 || end <= start) {
    return "- Add project-specific notes here.\n- This section is preserved across regenerations.";
  }

  const raw = existingContent.slice(start + MANUAL_START.length, end).trim();
  return raw || "- Add project-specific notes here.";
}

function renderMarkdown({ pkg, treeLines, importantFileStatus, manualNotes }) {
  const dependencies = Object.keys(pkg?.dependencies || {}).sort();
  const devDependencies = Object.keys(pkg?.devDependencies || {}).sort();
  const scripts = Object.entries(pkg?.scripts || {}).sort(([a], [b]) =>
    a.localeCompare(b)
  );

  const scriptsMd =
    scripts.length > 0
      ? scripts.map(([name, command]) => `- ${name}: \`${command}\``).join("\n")
      : "- (none)";

  return [
    "# PROJECT_CONTEXT",
    "",
    "This file is generated automatically to provide a compact, AI-friendly snapshot of the project.",
    "",
    "## Metadata",
    "",
    `- Project name: ${pkg?.name || "unknown"}`,
    `- Version: ${pkg?.version || "unknown"}`,
    `- Private: ${String(pkg?.private ?? "unknown")}`,
    "- Package manager: npm",
    "",
    "## Tech Stack",
    "",
    "- Runtime: Node.js",
    "- Frontend: React + Vite",
    "- Styling: Tailwind CSS",
    "- Testing: Vitest",
    "- Backend (optional): Supabase",
    "",
    "## NPM Scripts",
    "",
    scriptsMd,
    "",
    "## Dependencies",
    "",
    formatList(dependencies),
    "",
    "## Dev Dependencies",
    "",
    formatList(devDependencies),
    "",
    `## File Tree (depth <= ${TREE_MAX_DEPTH})`,
    "",
    treeLines.length > 0 ? treeLines.join("\n") : "- (empty)",
    "",
    "## Important Files Check",
    "",
    importantFileStatus,
    "",
    "## Quick Analysis Notes",
    "",
    "- This project currently has documentation in README and docs/ for setup and architecture details.",
    "- Supabase schema lives in supabase/schema.sql.",
    "- Domain utilities for personal payment date logic are in src/domain/personal/.",
    "",
    "## Sync Policy",
    "",
    "- PROJECT_CONTEXT.md is auto-generated by script.",
    "- Referenced files (README.md, docs/*.md, supabase/schema.sql) are source documents and are not auto-edited by this generator.",
    "- If code changes, update source docs/sql as part of the same change, then run `npm run context:update`.",
    "- Optional: keep `npm run context:watch` running during development so PROJECT_CONTEXT.md stays fresh.",
    "",
    "## Manual Notes (preserved)",
    "",
    MANUAL_START,
    manualNotes,
    MANUAL_END,
    "",
    "## Update Workflow",
    "",
    "- Manual refresh: `npm run context:update`",
    "- Continuous refresh while coding: `npm run context:watch`",
    "- Recommended for commits: run `npm run context:update` before commit, so this file always reflects latest state.",
    "",
  ].join("\n");
}

async function main() {
  const packageJsonPath = path.join(ROOT, "package.json");
  const pkg = await readJson(packageJsonPath);

  if (!pkg) {
    throw new Error("Could not read package.json. Run this script from project root.");
  }

  let existing = "";
  if (await pathExists(OUTPUT_FILE)) {
    existing = await fs.readFile(OUTPUT_FILE, "utf8");
  }

  const manualNotes = extractManualNotes(existing);
  const treeLines = await readDirectoryTree(ROOT, 0);
  const importantFileStatus = await collectImportantFileStatus();
  const markdown = renderMarkdown({
    pkg,
    treeLines,
    importantFileStatus,
    manualNotes,
  });

  if (existing === markdown) {
    console.log("PROJECT_CONTEXT.md unchanged.");
    return;
  }

  await fs.writeFile(OUTPUT_FILE, markdown, "utf8");
  console.log("PROJECT_CONTEXT.md updated.");
}

main().catch((error) => {
  console.error(error.message || error);
  process.exit(1);
});